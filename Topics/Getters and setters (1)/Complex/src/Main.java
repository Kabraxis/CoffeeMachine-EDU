
class Complex {

    private double real;
    private double imaginary;

    double getReal() {
        return this.real;
    }

    void setReal(double real) {
        this.real = real;
    }

    double getImaginary() {
        return this.imaginary;
    }

    void setImaginary(double imaginary) {
        this.imaginary = imaginary;
    }
}